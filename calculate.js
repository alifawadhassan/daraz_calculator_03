// function to calculate output
function calculate() {
  //
  //selling_price
  if (
    document.getElementById("selling_price").value != null ||
    document.getElementById("selling_price").value != ""
  ) {
    selling_price = parseFloat(document.getElementById("selling_price").value);
  } else {
    selling_price = 0;
  }

  //product_cost
  if (
    document.getElementById("product_cost").value != null ||
    document.getElementById("product_cost").value != ""
  ) {
    product_cost = parseFloat(document.getElementById("product_cost").value);
  } else {
    product_cost = 0;
  }

  //commission_perc
  temp_commission_perc = document.getElementById("commission_perc").value;
  commission_perc = temp_commission_perc.split("%")[0];

  //commission
  commission = (selling_price * commission_perc) / 100;
  document.getElementById("commission").value = commission.toFixed(2);

  //transaction_fees_perc
  temp_transaction_fees_perc = document.getElementById(
    "transaction_fees_perc"
  ).value;
  transaction_fees_perc = temp_transaction_fees_perc.split("%")[0];

  //transaction_fees
  transaction_fees = (selling_price * transaction_fees_perc) / 100;
  document.getElementById("transaction_fees").value =
    transaction_fees.toFixed(2);

  // vat per and value
  temp_vat_perc = document.getElementById("vat_perc").value;
  vat_perc = temp_vat_perc.split("%")[0];

  //
  vat = ((commission + transaction_fees) * vat_perc) / 100;
  document.getElementById("vat").value = vat.toFixed(2);

  //
  other_cost();
  // profit
  profit =
    selling_price -
    commission -
    product_cost -
    total_01 -
    transaction_fees -
    vat;
  document.getElementById("profit").value = profit.toFixed(1);

  // *****************************************************************
  //profit percentage
  profit_perc = (profit / selling_price) * 100;
  // document.getElementById("profit_perc").value = profit_perc.toFixed(2) + "%";

  if (profit_perc < 0) {
    temp = -1 * parseFloat(profit_perc.toFixed(1));
    dg_val[0] = temp;
    dg_val[1] = 100 - temp;
    text_cen = parseFloat(profit_perc.toFixed(1));
    graph_color = "#FF0000";
  } else {
    dg_val[0] = parseFloat(profit_perc.toFixed(1));
    dg_val[1] = 100 - profit_perc.toFixed(1);
    text_cen = parseFloat(profit_perc.toFixed(1));
    graph_color = "#30DD60";
  }

  //
  update_graph_01();

  // adding event listener onchange on input fields
  document
    .getElementById("selling_price")
    .addEventListener("change", calculate);
  document.getElementById("product_cost").addEventListener("change", calculate);

  //
  document
    .getElementById("catergory_dropdown")
    .addEventListener("change", calculate);
  document.getElementById("sell01").addEventListener("change", calculate);
  document.getElementById("sell02").addEventListener("change", calculate);

  //
  document
    .getElementById("cust_1__flayers")
    .addEventListener("change", calculate);
  document
    .getElementById("cust_2__bubble_wrap")
    .addEventListener("change", calculate);
  document
    .getElementById("cust_3__packaging")
    .addEventListener("change", calculate);
  document
    .getElementById("cust_4__printings")
    .addEventListener("change", calculate);
  document
    .getElementById("cust_5__drop_off")
    .addEventListener("change", calculate);
  document
    .getElementById("cust_6__othercost")
    .addEventListener("change", calculate);
}

//Function To Draw Bar Graph
function initialize_graph_01() {
  //Code for Bar Graph
  var canvasElement = document.getElementById("mychart01");
  canvasElement.width = 50;
  canvasElement.height = 50;
  var ctx = canvasElement.getContext("2d");
  var data = {
    labels: ["Profit %"],
    datasets: [
      {
        data: dg_val,
        backgroundColor: [graph_color, "#FFFFFF"],
        hoverBackgroundColor: [graph_color, "#FFFFFF"],
        borderWidth: 1,
      },
    ],
  };
  var config = {
    type: "doughnut",
    data: data,
    options: {
      responsive: true,
      legend: {
        display: false,
      },
      cutoutPercentage: 70,
      rotation: 1 * Math.PI,
      circumference: 1 * Math.PI,
    },
  };

  Chart.pluginService.register({
    beforeDraw: function (chart) {
      if (chart.chart.canvas.id == "mychart01") {
        var width = chart.chart.width,
          height = chart.chart.height,
          ctx = chart.chart.ctx;

        ctx.restore();
        var fontSize = (height / 114).toFixed(2);
        ctx.font = fontSize + "em sans-serif";
        ctx.textBaseline = "middle";
        ctx.fillStyle = "white";

        var text = text_cen + " %",
          textX = Math.round((width - ctx.measureText(text).width) / 2),
          textY = height / 1.5;

        ctx.fillText(text, textX, textY);
        ctx.save();
      }
    },
  });

  chart00 = new Chart(canvasElement, config);
}

